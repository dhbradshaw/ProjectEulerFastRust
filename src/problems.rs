fn greatest_multiple(v: Vec<u64>, n: usize) -> u64 {
    let l = v.len();
    let mut start = 0;
    let mut largest = 0;
    while (start + n) <= l {
        let multiple = &v[start..start + n].iter()
            .fold(1, |multiple, e| {multiple * (*e) as u64});
        largest = max(largest, *multiple);
        start += 1;
    }
    largest
}

