fn cycle_size(den: usize) -> usize {
    let mut num: usize = 1;
    let mut map = HashMap::new();
    let mut index: usize = 0;
    let mut last_index: usize;
    loop {
        num = (num % den) * 10;
        last_index = *map.entry(num).or_insert(index);
        if index != last_index {
            break index - last_index
        }
        index += 1;
    }
}

