#[allow(dead_code)]
pub fn pandigital_multiple(n: u64) -> u64 {
    let mut n = n;
    let mut m = 2;
    while places(n) < 9 {
        let next_multiple = m * n;
        n *= 10u64.pow(places(next_multiple));
        n += next_multiple;
        m += 1;
    }
    if places(n) > 10 {
        return 0;
    }

    match  pan_ok(n) {
        true => n,
        _ => 0
    }
}

